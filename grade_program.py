# برنامج نظام التقديرات المتكامل للتعرف على تقدير الطالب
# التفكير البرمجي: حلقة تكرارية مستمرة، معالجة أخطاء الإدخال، وشروط منطقية متسلسلة.

def main():
    """الدالة الرئيسية لتشغيل البرنامج."""
    
    # رسالة ترحيبية وتعليمات المستخدم
    print("------------------------------------------")
    print("  *** نظام تقدير درجات الطلاب الذكي ***  ")
    print("تعليمات: أدخل درجة الطالب (0-100) للتعرف على التقدير.")
    print("أدخل كلمة 'exit' أو 'خروج' للإنهاء في أي وقت.")
    print("------------------------------------------")

    # استمرار عمل البرنامج طالما لم يقم المستخدم بإدخال كلمة "خروج"
    while True:
        try:
            # 1. طلب المدخلات
            user_input = input("\nيرجى إدخال درجة الطالب: ").strip().lower()

            # 2. التحقق من رغبة المستخدم في الخروج
            if user_input == 'exit' or user_input == 'خروج':
                print("\n[!] يتم الآن إغلاق البرنامج... شكراً لاستخدامك النظام.")
                break

            # 3. التغلب على المشاكل (معالجة قيم غير متوقعة): محاولة تحويل المدخلات لرقم
            grade = float(user_input)

            # 4. التغلب على المشاكل (معالجة نطاق الدرجة)
            if grade < 0 or grade > 100:
                print("[-] خطأ: الدرجة يجب أن تكون رقمية وفي النطاق بين 0 و 100 فقط.")
                continue

            # 5. منطق تحديد التقدير (شروط متسلسلة)
            if grade >= 90:
                result = "ممتاز"
            elif grade >= 80:
                result = "جيد جداً"
            elif grade >= 65:
                result = "جيد"
            else:
                # هذا يشمل جميع الدرجات أقل من 65
                result = "ضعيف"

            # 6. إظهار النتيجة للمستخدم
            print(f"[+] تقدير الطالب بناءً على الدرجة {grade} هو: ({result})")

        except ValueError:
            # التغلب على المشاكل (إدخال نص بدلاً من رقم)
            print("[-] خطأ: المدخلات غير صالحة. يرجى إدخال درجة رقمية أو 'exit'.")

if __name__ == "__main__":
    main()
